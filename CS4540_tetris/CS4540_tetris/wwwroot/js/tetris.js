function drawSquare(x, y, color) {
    graphics.beginFill(color);
    graphics.drawRect(x*SQ, y*SQ, SQ, SQ); // drawRect(x, y, width, height)
    graphics.endFill();
}

let board = [];
for (r = 0; r < ROW; r++) {
    board[r] = [];
    for (c = 0; c < COL; c++) {
        board[r][c] = 0x000000;
    }
}

//visual testing of the board creation
board[0][0] = 0x00FF00;
board[19][9] = 0xFF0000;

// draw the board
function drawBoard() {
    graphics.clear();
    for (r = 0; r < ROW; r++) {
        for (c = 0; c < COL; c++) {
            drawSquare(c, r, board[r][c]);
        }
    }
    app.stage.addChild(graphics);
}

